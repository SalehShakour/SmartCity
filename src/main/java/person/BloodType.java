package person;

/**
 * Enumeration representing different blood types.
 * <p>
 * Blood types include:
 * - A_positive
 * - B_positive
 * - O_positive
 * - AB_positive
 * - A_negative
 * - B_negative
 * - O_negative
 * - AB_negative
 */
public enum BloodType {
    A_positive, B_positive, O_positive, AB_positive, A_negative, B_negative, O_negative, AB_negative
}
